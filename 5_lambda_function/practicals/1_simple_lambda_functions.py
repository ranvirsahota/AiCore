# 1. A lambda function that takes in a number and returns that number squared 
sqaured = lambda x: x ** 2
print(sqaured(5))

# 2. A lambda function that takes in a number and returns that number cubed
cubed = lambda x: x ** 3
print(cubed(5))

# 3. A lambda function that takes two numbers and returns the sum of those numbers
addition = lambda x, y: x + y
print(addition(3, 7))

# 4. A lambda function that takes two numbers and returns the product of those numbers
product = lambda x, y: x * y
print(product(2, 5))